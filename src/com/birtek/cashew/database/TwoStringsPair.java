package com.birtek.cashew.database;

public class TwoStringsPair {
    private final String first, second;

    public TwoStringsPair(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
